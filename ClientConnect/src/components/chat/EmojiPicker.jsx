const EmojiPicker = ({ onEmojiSelect, onClose, isOpen }) => {
  const [activeCategory, setActiveCategory] = useState('smileys');
  const [recentEmojis, setRecentEmojis] = useState([]);

  const handleEmojiClick = (emoji) => {
    // Add to recent emojis
    const newRecent = [emoji, ...recentEmojis.filter(e => e !== emoji)].slice(0, 20);
    setRecentEmojis(newRecent);
    localStorage.setItem('recentEmojis', JSON.stringify(newRecent));
    
    onEmojiSelect(emoji);
  };

  const loadRecentEmojis = () => {
    const stored = localStorage.getItem('recentEmojis');
    if (stored) {
      setRecentEmojis(JSON.parse(stored));
    }
  };

  React.useEffect(() => {
    if (isOpen) {
      loadRecentEmojis();
    }
  }, [isOpen]);

  if (!isOpen) return null;

  return (
    <div className="fixed inset-0 bg-black bg-opacity-50 z-50 flex items-center justify-center">
      <div className="bg-white rounded-lg shadow-xl w-full max-w-sm">
        <div className="p-4 border-b border-gray-200">
          <div className="flex items-center justify-between">
            <h3 className="text-lg font-semibold">Choose Emoji</h3>
            <button
              onClick={onClose}
              className="text-gray-400 hover:text-gray-600"
            >
              <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
              </svg>
            </button>
          </div>
        </div>

        <div className="p-4">
          {/* Category tabs */}
          <div className="flex flex-wrap gap-1 mb-4">
            {Object.entries(EMOJI_CATEGORIES).map(([category, emoji]) => (
              <button
                key={category}
                onClick={() => setActiveCategory(category)}
                className={`p-2 rounded-lg text-lg ${
                  activeCategory === category
                    ? 'bg-indigo-100 text-indigo-600'
                    : 'hover:bg-gray-100'
                }`}
                title={category}
              >
                {emoji}
              </button>
            ))}
          </div>

          {/* Emoji grid */}
          <div className="max-h-64 overflow-y-auto">
            <div className="grid grid-cols-8 gap-1">
              {(activeCategory === 'recent' ? recentEmojis : EMOJIS[activeCategory] || []).map((emoji, index) => (
                <button
                  key={index}
                  onClick={() => handleEmojiClick(emoji)}
                  className="p-2 text-lg hover:bg-gray-100 rounded-lg"
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default EmojiPicker;
